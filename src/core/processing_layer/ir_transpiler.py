    """C++ 转译器."""

    def target_language(self) -> TargetLanguage:
        return TargetLanguage.CPP

    def transpile(self, ir_tree: IRNode) -> IROutput:
        """转译为 C++.

        输出:
            #include <iostream>
            int main() {
                std::cout << "Hello World" << std::endl;
                return 0;
            }
        """
        literal = (
            ir_tree.children[0].children[0].value
            if ir_tree.children and ir_tree.children[0].children
            else ""
        )
        code = (
            "// Auto-generated by Super-HelloWorld IR Transpiler\n"
            "#include <iostream>\n"
            "\n"
            "int main() {\n"
            f'    std::cout << "{literal}" << std::endl;\n'
            "    return 0;\n"
            "}\n"
        )
        return IROutput(
            target_language=TargetLanguage.CPP,
            source_code=code,
            ast_representation={
                "type": "TranslationUnit",
                "declarations": [
                    {
                        "type": "FunctionDecl",
                        "name": "main",
                        "body": [
                            {
                                "type": "ExprStmt",
                                "expr": {
                                    "type": "Operator<<",
                                    "left": {
                                        "type": "Operator<<",
                                        "left": "std::cout",
                                        "right": literal,
                                    },
                                    "right": "std::endl",
                                },
                            },
                            {"type": "ReturnStmt", "value": 0},
                        ],
                    }
                ],
            },
            source_map=self.generate_source_map(literal, code),
        )


