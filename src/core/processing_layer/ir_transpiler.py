class WasmTranspiler(AbstractTranspiler):
    """WebAssembly 转译器 (WAT 格式)."""

    def target_language(self) -> TargetLanguage:
        return TargetLanguage.WASM

    def transpile(self, ir_tree: IRNode) -> IROutput:
        """转译为 WAT (WebAssembly Text Format).

        输出保存为 .wat 文件，可编译为 .wasm。
        """
        literal = (
            ir_tree.children[0].children[0].value
            if ir_tree.children and ir_tree.children[0].children
            else ""
        )
        code = (
            ";; Auto-generated by Super-HelloWorld IR Transpiler\n"
            "(module\n"
            '  (import "console" "log" (func $log (param i32 i32)))\n'
            '  (import "js" "mem" (memory 1))\n'
            f'  (data (i32.const 0) "{literal}")\n'
            '  (func (export "hello_world")\n'
            f"    (call $log\n"
            f"      (i32.const 0)\n"
            f"      (i32.const {len(literal)}))\n"
            "  )\n"
            ")\n"
        )
        return IROutput(
            target_language=TargetLanguage.WASM,
            source_code=code,
            ast_representation={
                "type": "Module",
                "imports": [{"module": "console", "name": "log"}],
                "exports": [{"name": "hello_world"}],
                "data": [{"offset": 0, "value": literal}],
            },
            source_map={},
        )


# ---- 转译器注册表 ----

_TRANSPILER_REGISTRY: dict[TargetLanguage, AbstractTranspiler] = {
    TargetLanguage.JAVASCRIPT: JavaScriptTranspiler(),
    TargetLanguage.JAVA: JavaTranspiler(),
    TargetLanguage.CPP: CppTranspiler(),
    TargetLanguage.RUST: RustTranspiler(),
    TargetLanguage.WASM: WasmTranspiler(),
}


class IRTranspiler:
    """IR 转译器 — 多语言中间表示转译入口.

    使用示例:
        >>> transpiler = IRTranspiler()
        >>> js_output = transpiler.transpile_to(TargetLanguage.JAVASCRIPT)
        >>> print(js_output.source_code)
        // Auto-generated by Super-HelloWorld IR Transpiler
        console.log("Hello World");

        >>> all_outputs = transpiler.transpile_all()
        >>> for lang, output in all_outputs.items():
        ...     print(f"{lang.value}: {len(output.source_code)} chars")
    """

    def __init__(self) -> None:
        """初始化 IR 转译器."""
        self._builder: IRBuilder = IRBuilder()
        self._message: str = "Hello World"
        self._transpile_count: int = 0

    # ---- 公共 API ----

    def transpile_to(self, target: TargetLanguage, message: str | None = None) -> IROutput:
        """转译为指定语言.

        Args:
            target: 目标语言.
            message: 自定义消息 (默认 Hello World).

        Returns:
            IROutput 对象.

        Raises:
            ValueError: 不支持的目标语言.
        """
        transpiler = _TRANSPILER_REGISTRY.get(target)
        if transpiler is None:
            raise ValueError(f"Unsupported target language: {target}")

        msg = message or self._message
        ir_tree = self._builder.build_custom(msg) if message else self._builder.build_hello_world()
        output = transpiler.transpile(ir_tree)
        self._transpile_count += 1
        return output

    def transpile_all(self, message: str | None = None) -> dict[TargetLanguage, IROutput]:
        """转译为所有支持的语言.

        Args:
            message: 自定义消息.

        Returns:
            {语言: IROutput} 映射.
        """
        results: dict[TargetLanguage, IROutput] = {}
        for lang in TargetLanguage:
            with contextlib.suppress(ValueError):
                results[lang] = self.transpile_to(lang, message)
        return results

    def get_ir_tree(self, message: str | None = None) -> IRNode:
        """获取 IR 树 (不进行转译).

        Args:
            message: 自定义消息.

        Returns:
            IR 树根节点.
        """
        if message:
            return self._builder.build_custom(message)
        return self._builder.build_hello_world()

    def export_ir_json(self, message: str | None = None) -> str:
        """导出 IR 树为 JSON.

        Args:
            message: 自定义消息.

        Returns:
            JSON 字符串.
        """
        ir_tree = self.get_ir_tree(message)
        return json.dumps(
            self._serialize_ir(ir_tree),
            indent=2,
            ensure_ascii=False,
        )

    def export_all_to_files(self, output_dir: str = "./output") -> dict[TargetLanguage, str]:
        """将所有转译结果导出到文件.

        需要在实际文件系统中运行时使用。

        Args:
            output_dir: 输出目录.

        Returns:
            {语言: 文件路径} 映射.
        """
        import os

        os.makedirs(output_dir, exist_ok=True)
        ext_map = {
            TargetLanguage.JAVASCRIPT: ".js",
            TargetLanguage.JAVA: ".java",
            TargetLanguage.CPP: ".cpp",
            TargetLanguage.RUST: ".rs",
            TargetLanguage.WASM: ".wat",
            TargetLanguage.PYTHON: ".py",
            TargetLanguage.GO: ".go",
            TargetLanguage.CSHARP: ".cs",
        }

        results: dict[TargetLanguage, str] = {}
        outputs = self.transpile_all()

        for lang, output in outputs.items():
            ext = ext_map.get(lang, ".txt")
            filename = f"hello_world{ext}"
            filepath = os.path.join(output_dir, filename)
            with open(filepath, "w", encoding="utf-8") as f:
                f.write(output.source_code)
            results[lang] = filepath

        return results

    # ---- 内部 ----

    @staticmethod
    def _serialize_ir(node: IRNode) -> dict[str, Any]:
        """递归序列化 IR 树."""
        return {
            "node_type": node.node_type,
            "value": node.value,
            "metadata": node.metadata,
            "children": [IRTranspiler._serialize_ir(child) for child in node.children],
        }

    # ---- 属性 ----

    @property
    def message(self) -> str:
        """当前消息."""
        return self._message

    @message.setter
    def message(self, value: str) -> None:
        self._message = value

    @property
    def transpile_count(self) -> int:
        """转译次数."""
        return self._transpile_count

    @property
    def supported_languages(self) -> list[TargetLanguage]:
        """支持的目标语言列表."""
        return list(_TRANSPILER_REGISTRY.keys())

    @classmethod
    def register_transpiler(
        cls,
        language: TargetLanguage,
        transpiler: AbstractTranspiler,
    ) -> None:
        """注册自定义转译器.

        Args:
            language: 目标语言.
            transpiler: 转译器实例.
        """
        _TRANSPILER_REGISTRY[language] = transpiler
