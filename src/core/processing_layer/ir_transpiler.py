"""IR 转译器 — 将 Hello World 转换为多语言中间表示.

支持的目标语言:
    - JavaScript (Node.js / Browser)
    - Java (JVM / Android)
    - C++ (GCC / Clang / MSVC)
    - Rust (Safe)
    - WASM (WebAssembly Text Format)

所有转译基于中间 IR 树，确保跨平台一致性。
"""

from __future__ import annotations

import contextlib
import json
import time
from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from enum import Enum
from typing import Any


class TargetLanguage(Enum):
    """目标语言枚举."""

    JAVASCRIPT = "javascript"
    JAVA = "java"
    CPP = "cpp"
    RUST = "rust"
    WASM = "wasm"
    PYTHON = "python"
    GO = "go"
    CSHARP = "csharp"


# ---- IR 数据结构 ----


@dataclass(frozen=True)
class IRNode:
    """IR 节点 (不可变)."""

    node_type: str  # program / statement / expression / literal
    value: str
    children: list[IRNode] = field(default_factory=list)
    metadata: dict[str, str] = field(default_factory=dict)


@dataclass(frozen=True)
class IROutput:
    """IR 输出结果."""

    target_language: TargetLanguage
    source_code: str
    ast_representation: dict[str, Any]
    source_map: dict[int, int]  # 源位置 → 目标位置
    encoding: str = "UTF-8"
    compiled_at: float = field(default_factory=time.time)


# ---- 抽象转译器 ----


class AbstractTranspiler(ABC):
    """转译器抽象基类."""

    @abstractmethod
    def transpile(self, ir_tree: IRNode) -> IROutput:
        """将 IR 树转译为源代码.

        Args:
            ir_tree: 中间表示树.

        Returns:
            包含目标源代码和元数据的 IROutput.
        """
        ...

    @abstractmethod
    def target_language(self) -> TargetLanguage:
        """返回目标语言."""
        ...

    def generate_source_map(self, source: str, target: str) -> dict[int, int]:
        """生成源映射."""
        return {i: i for i in range(min(len(source), len(target)))}


# ---- IR 树构建器 ----


class IRBuilder:
    """IR 树构建器 — 构建 Hello World 的标准化 IR 表示."""

    _HELLO_WORLD_MESSAGE: str = "Hello World"

    def build_hello_world(self) -> IRNode:
        """构建 Hello World 的 IR 表示.

        IR 结构:
            program
              └── statement (function_call: print)
                    └── literal: "Hello World"
        """
        literal = IRNode(
            node_type="literal",
            value=self._HELLO_WORLD_MESSAGE,
            metadata={"type": "string", "length": str(len(self._HELLO_WORLD_MESSAGE))},
        )
        statement = IRNode(
            node_type="statement",
            value="print",
            children=[literal],
            metadata={"function": "output", "side_effects": "true"},
        )
        program = IRNode(
            node_type="program",
            value="hello_world",
            children=[statement],
            metadata={"author": "Enterprise", "version": "1.0.0"},
        )
        return program

    def build_custom(self, message: str) -> IRNode:
        """构建自定义消息的 IR 表示.

        Args:
            message: 自定义消息.

        Returns:
            IR 树根节点.
        """
        literal = IRNode(
            node_type="literal",
            value=message,
            metadata={"type": "string", "length": str(len(message))},
        )
        statement = IRNode(
            node_type="statement",
            value="print",
            children=[literal],
        )
        return IRNode(
            node_type="program",
            value="custom_output",
            children=[statement],
        )


# ---- 具体转译器实现 ----


class JavaScriptTranspiler(AbstractTranspiler):
    """JavaScript 转译器."""

    def target_language(self) -> TargetLanguage:
        return TargetLanguage.JAVASCRIPT

    def transpile(self, ir_tree: IRNode) -> IROutput:
        """转译为 JS.

        输出:
            // Auto-generated by Super-HelloWorld IR Transpiler
            console.log("Hello World");
        """
        literal = (
            ir_tree.children[0].children[0].value
            if ir_tree.children and ir_tree.children[0].children
            else ""
        )
        code = f'// Auto-generated by Super-HelloWorld IR Transpiler\nconsole.log("{literal}");\n'
        return IROutput(
            target_language=TargetLanguage.JAVASCRIPT,
            source_code=code,
            ast_representation={
                "type": "Program",
                "body": [
                    {
                        "type": "ExpressionStatement",
                        "expression": {
                            "type": "CallExpression",
                            "callee": {
                                "type": "MemberExpression",
                                "object": "console",
                                "property": "log",
                            },
                            "arguments": [{"type": "Literal", "value": literal}],
                        },
                    }
                ],
            },
            source_map=self.generate_source_map(literal, code),
        )


class JavaTranspiler(AbstractTranspiler):
    """Java 转译器."""

    def target_language(self) -> TargetLanguage:
        return TargetLanguage.JAVA

    def transpile(self, ir_tree: IRNode) -> IROutput:
        """转译为 Java.

        输出:
            public class HelloWorld {
                public static void main(String[] args) {
                    System.out.println("Hello World");
                }
            }
        """
        literal = (
            ir_tree.children[0].children[0].value
            if ir_tree.children and ir_tree.children[0].children
            else ""
        )
        code = (
            "// Auto-generated by Super-HelloWorld IR Transpiler\n"
            "public class HelloWorld {\n"
            "    public static void main(String[] args) {\n"
            f'        System.out.println("{literal}");\n'
            "    }\n"
            "}\n"
        )
        return IROutput(
            target_language=TargetLanguage.JAVA,
            source_code=code,
            ast_representation={
                "type": "CompilationUnit",
                "types": [
                    {
                        "type": "ClassDeclaration",
                        "name": "HelloWorld",
                        "methods": [
                            {
                                "type": "MethodDeclaration",
                                "name": "main",
                                "body": [
                                    {
                                        "type": "MethodInvocation",
                                        "target": "System.out",
                                        "method": "println",
                                        "arguments": [literal],
                                    }
                                ],
                            }
                        ],
                    }
                ],
            },
            source_map=self.generate_source_map(literal, code),
        )


